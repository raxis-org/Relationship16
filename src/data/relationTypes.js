// スコアから4文字コードを生成（互換性のため残す）
export function generateTypeCode(scores) {
  const code = [
    scores.P >= 0 ? 'E' : 'H',
    scores.M >= 0 ? 'B' : 'I',
    scores.G >= 0 ? 'S' : 'A',
    scores.V >= 0 ? 'C' : 'D',
  ].join('');
  return code;
}

